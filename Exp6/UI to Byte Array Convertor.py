import gzip
import io
import textwrap

def html_to_c_gzip_array(html_content, array_name="index_ov2640_html_gz", define_name="index_ov2640_html_gz_len"):
    """
    Compresses HTML content using gzip and formats it into a C-style byte array.

    Args:
        html_content (str): The HTML string to be compressed.
        array_name (str): The name for the C const unsigned char array.
        define_name (str): The name for the C #define length constant.

    Returns:
        str: A string containing the C header file content.
    """
    
    html_bytes = html_content.encode('utf-8')
    buf = io.BytesIO()
    
    with gzip.GzipFile(fileobj=buf, mode='wb', mtime=0) as f:
        f.write(html_bytes)
    
    gzip_bytes = buf.getvalue()

    
    data_len = len(gzip_bytes)

    
    hex_list = [f"0x{b:02x}" for b in gzip_bytes]
    
    
    formatted_hex = []
    for i in range(0, data_len, 16):
        line = ", ".join(hex_list[i:i+16])
        
        formatted_hex.append(f"  {line},")
    
    if formatted_hex:
        last_line = formatted_hex[-1]
        formatted_hex[-1] = last_line.rstrip(',')

    array_body = "\n".join(formatted_hex)

    c_array_template = f"""\
// This file was automatically generated by a Python script
// Do NOT modify manually!

// --- Custom UI DEFINITION ---
#define {define_name} {data_len}

const unsigned char {array_name}[] = {{
{array_body}
}};

// --- DUMMY DEFINITIONS FOR OTHER MODELS (TO PREVENT COMPILATION ERRORS in app_httpd.cpp) ---
// Your sketch seems to check for OV3660 and OV5640, but you only provided one UI.
// We include these empty definitions to satisfy the compiler checks.
#define index_ov3660_html_gz_len 0
const unsigned char index_ov3660_html_gz[] = {{}};

#define index_ov5640_html_gz_len 0
const unsigned char index_ov5640_html_gz[] = {{}};
"""
    return c_array_template


